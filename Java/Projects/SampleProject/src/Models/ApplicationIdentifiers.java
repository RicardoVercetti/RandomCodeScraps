package Models;

public class ApplicationIdentifiers {
    
	
	String application_identifier, vendor, country, name, description, type;

	public String getApplication_identifier() {
		return application_identifier;
	}

	public void setApplication_identifier(String application_identifier) {
		this.application_identifier = application_identifier;
	}

	public String getVendor() {
		return vendor;
	}

	public void setVendor(String vendor) {
		this.vendor = vendor;
	}

	public String getCountry() {
		return country;
	}

	public void setCountry(String country) {
		this.country = country;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}


}
